package core.telegram.model;


/**
 * Перерахування (Enum) конфігураційних параметрів для роботи Telegram-бота.
 * <p>
 * Зберігає статичні константи, необхідні для авторизації бота та вказівки цільових
 * чатів (каналів та груп обговорення) для публікації оголошень нерухомості.
 * </p>
 * <p>
 * <i>Примітка:</i> Для підвищення безпеки у великих проектах ці токени та ідентифікатори
 * рекомендується виносити у зовнішні конфігураційні файли (наприклад, {@code config.properties},
 * {@code .env} або системні змінні середовища), проте використання констант всередині Enum
 * є чудовим і швидким рішенням для локальних інструментів автоматизації.
 * </p>
 * * @author Mykola
 */
public enum Config {

    /** * API-токен Telegram-бота, отриманий від BotFather.
     * Використовується для авторизації та виконання запитів до Telegram Bot API.
     */
    TOKEN("8689197100:AAGGoRNUWrMd2w-XPE9xvA9-NLd0S2w4Vkk"),

    /** * Юзернейм (системне ім'я) вашого Telegram-бота.
     */
    NAME("RealEstateManager_chernovtsy_bot"),

    /** * Унікальний ідентифікатор (ID) Telegram-каналу, куди бот публікує основні пости.
     * Значення починається з {@code -100}, що є стандартним префіксом для супергруп та каналів у Telegram.
     */
    ID_CHAT("-1003741489061"),

    /** * Унікальний ідентифікатор (ID) групи обговорення (чату коментарів), яка зв'язана з каналом.
     * Використовується для надсилання фотографій у вигляді реплаю (коментаря) до скопійованого туди поста.
     */
    ID_CHAT_COMMENT("-1004460622171");

    /** Текстове значення конфігураційного ключа */
    private final String key;

    /**
     * Конструктор перерахування для ініціалізації конфігураційних параметрів.
     *
     * @param key строкове значення параметра (токен або ID чату)
     */
    Config(String key) {
        this.key = key;
    }

    /**
     * Повертає збережене текстове значення конфігураційного параметра.
     *
     * @return рядок із токеном, ім'ям або ідентифікатором чату
     */
    public String getKey() {
        return key;
    }
}
