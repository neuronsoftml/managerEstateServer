package core.telegram.model;

/**
 * Перерахування (Enum) станів діалогу користувача з Telegram-ботом.
 * <p>
 * Використовується для реалізації архітектури скінченного автомата (Finite State Machine - FSM).
 * Дозволяє боту точно знати, на якому кроці взаємодії перебуває конкретний користувач,
 * та як саме інтерпретувати його наступне текстове повідомлення чи клік по кнопці.
 * </p>
 * <p>
 * Стани розділені на два основні блоки:
 * <ol>
 * <li><b>Загальний фільтр пошуку:</b> швидкий пошук нерухомості за категоріями, містами та ціною.</li>
 * <li><b>Анкета клієнта (Wizard):</b> детальний покроковий збір інформації про орендаря чи покупця.</li>
 * </ol>
 * </p>
 * * @author Mykola
 */
public enum BotState {

    /** * Стартовий стан бота. Користувач щойно запустив бота через команду /start. */
    START,

    /** * Очікування вибору типу угоди: купівля чи оренда нерухомості. */
    WAITING_FOR_DEAL_TYPE,

    /** * Очікування вибору типу нерухомості (Квартира, Будинок, Комерція, Ділянка). */
    WAITING_FOR_PROPERTY_TYPE,

    /** * Очікування вибору або введення міста пошуку. */
    WAITING_FOR_CITY,

    /** * Відображення результатів пошуку, що відповідають вибраним фільтрам. */
    SHOWING_RESULTS,

    /** * Очікування вибору кількості кімнат (застосовується переважно для квартир: 1, 2, 3, 3+). */
    WAITING_FOR_ROOMS,

    /** * Очікування вибору або введення цінового діапазону в доларах ($). */
    WAITING_FOR_PRICE,

    /** * Головне меню бота. Базовий стан навігації після авторизації чи завершення пошуків. */
    MAIN_MENU,


    // ── Секція опитування анкети пошуку житла (Wizard / Покроковий майстер) ──

    /** * Крок 1: Очікування введення імені користувача для профілю. */
    PROFILE_WAITING_NAME,

    /** * Крок 2: Очікування введення або надання номеру телефону. */
    PROFILE_WAITING_PHONE,

    /** * Крок 3: Очікування визначення максимального місячного бюджету на житло. */
    PROFILE_WAITING_BUDGET,

    /** * Крок 4 (Фінанси): Очікування підтвердження готовності сплатити заставу власнику (депозит). */
    PROFILE_WAITING_DEPOSIT,

    /** * Крок 5 (Фінанси): Очікування згоди на сплату комісії ріелтору за послуги. */
    PROFILE_WAITING_COMMISSION,

    /** * Крок 6: Очікування вибору бажаних районів міста для проживання. */
    PROFILE_WAITING_DISTRICTS,

    /** * Крок 7: Очікування вибору бажаної кількості кімнат у квартирі/будинку. */
    PROFILE_WAITING_ROOMS,

    /** * Крок 8: Очікування визначення терміну оренди (наприклад, від 6 місяців, подобово тощо). */
    PROFILE_WAITING_TERM,

    /** * Крок 9: Очікування інформації про склад проживаючих (кількість орендарів, родина тощо). */
    PROFILE_WAITING_TENANTS,

    /** * Крок 10: Збір інформації про наявність та вік дітей. */
    PROFILE_WAITING_CHILDREN,

    /** * Крок 11: Збір інформації про наявність домашніх улюбленців (собаки, коти тощо). */
    PROFILE_WAITING_PETS,

    /** * Крок 12: Очікування даних про сферу зайнятості/роботу клієнта. */
    PROFILE_WAITING_EMPLOYMENT,

    /** * Крок 13: Очікування формату роботи (дистанційно, офіс, позмінно). */
    PROFILE_WAITING_WORK_FORMAT,

    /** * Крок 14: Питання про ставлення до паління всередині житла. */
    PROFILE_WAITING_SMOKING,

    /** * Крок 15: Очікування інформації про наявність власного автомобіля (потреба в паркомісці). */
    PROFILE_WAITING_CAR,

    /** * Крок 16: Очікування опису критичних вимог до житла (наприклад, автономне опалення, поверх тощо). */
    PROFILE_WAITING_CRITICAL,

    /** * Крок 17: Фінальне підтвердження заповненої анкети користувачем перед збереженням у базу даних. */
    PROFILE_CONFIRMATION,




    // ── Секція опитування анкети продажу/здачі в оренду  (Wizard / Покроковий майстер) ──

    /** * Крок 1: Очікування вибору типу угоди (Купівля/Продаж чи Оренда). */
    CREATE_POST_WAITING_FOR_DEAL_TYPE,

    /** * Крок 2: Очікування вибору типу нерухомості (Квартира, Будинок, Комерція, Ділянка). */
    CREATE_POST_WAITING_FOR_PROPERTY_TYPE,

    /** * Крок 2.1: Очікування вибору типу ринку (Новобудова / Вторинний ринок). Йде одразу після типу нерухомості. */
    CREATE_POST_WAITING_FOR_MARKET_TYPE,

    /** * Крок 3: Очікування введення назви (заголовку) оголошення. */
    CREATE_POST_WAITING_FOR_TITLE,

    /** * Крок 4: Очікування вибору валюти ціни (USD/EUR/UAH). */
    CREATE_POST_WAITING_FOR_CURRENCY,

    /** * Крок 5: Очікування введення числового значення ціни. */
    CREATE_POST_WAITING_FOR_PRICE,

    /** * Крок 6: Очікування введення текстового опису локації (район/адреса). */
    CREATE_POST_WAITING_FOR_LOCATION,

    /** * Крок 7 (*): Очікування площі земельної ділянки. Лише для Будинку та Ділянки. */
    CREATE_POST_WAITING_FOR_LAND_AREA,

    /** * Крок 8 (*): Очікування кількості кімнат. Лише для Квартири та Будинку. */
    CREATE_POST_WAITING_FOR_ROOMS,

    /** * Крок 9 (*): Очікування загальної площі об'єкта. Для всіх типів, крім Ділянки. */
    CREATE_POST_WAITING_FOR_AREA,

    /** * Крок 10 (*): Очікування номеру поверху. Для всіх типів, крім Ділянки. */
    CREATE_POST_WAITING_FOR_FLOOR,

    /** * Крок 10.1 (*): Очікування загальної поверховості будинку. Йде одразу після поверху, для тих самих типів. */
    CREATE_POST_WAITING_FOR_TOTAL_FLOORS,

    /** * Крок 11: Очікування вибору типу продавця (Приватна особа / Бізнес). */
    CREATE_POST_WAITING_FOR_SELLER_TYPE,

    /** * Крок 12: Очікування вибору терміну публікації оголошення (3, 7, 15 чи 30 днів). */
    CREATE_POST_WAITING_FOR_DURATION,

    /** * Крок 13: Очікування введення текстового опису оголошення. */
    CREATE_POST_WAITING_FOR_DESCRIPTION,

    /** * Крок 14: Очікування завантаження фотографій об'єкта (або натискання кнопки "Готово"). */
    CREATE_POST_WAITING_FOR_PHOTOS,

    /** * Крок 14.1: Очікування номеру телефону через кнопку "Поділитись контактом" (RequestContact). */
    CREATE_POST_WAITING_FOR_PHONE,

    /** * Крок 15: Показ підсумкового резюме анкети з кнопками редагування полів та підтвердження. */
    CREATE_POST_SUMMARY,

    /** * Фінальний стан: користувач підтвердив анкету й очікує здійснення оплати за розміщення. */
    CREATE_POST_WAITING_FOR_PAYMENT,
}
