package core.tools.parser.olx;

import java.util.List;

/**
 * Утилітарний парсер технічних характеристик (параметрів) оголошень нерухомості.
 * <p>
 * Клас призначений для вилучення та аналізу числових параметрів із текстового списку характеристик,
 * отриманих під час парсингу сторінки OLX (наприклад, кількість кімнат, поверх, загальна площа).
 * </p>
 * <p>
 * Особливості реалізації:
 * <ul>
 * <li>Усі методи безпечні до передачі {@code null} або відсутності параметрів — у таких випадках повертається {@code -1}.</li>
 * <li>Для вилучення чисел використовується фільтрація регулярними виразами, що дозволяє ігнорувати текст (наприклад, перетворює "55.4 м²" на "55.4").</li>
 * <li>Помилки невідповідності форматів безпечно перехоплюються, не порушуючи загальний потік виконання програми.</li>
 * </ul>
 * </p>
 * * @author Mykola
 */
public class AnnouncementParamParser {

    /**
     * Вилучає кількість кімнат зі списку параметрів оголошення.
     * <p>
     * Шукає параметр, що починається з префікса "Кількість кімнат:", очищує його від нецифрових символів
     * та конвертує в ціле число.
     * </p>
     *
     * @param params список текстових параметрів оголошення (наприклад, ["Поверх: 3", "Кількість кімнат: 2"])
     * @return кількість кімнат, або {@code -1}, якщо параметр не знайдено чи його неможливо розпарсити
     */
    public static int getRoomsCount(List<String> params) {
        return extractInteger(params, "Кількість кімнат:");
    }

    /**
     * Вилучає номер поверху зі списку параметрів оголошення.
     * <p>
     * Шукає параметр, що починається з префікса "Поверх:" (типово для квартир).
     * Якщо його немає — пробує "Поверховість:" (типовий параметр для будинків,
     * що позначає кількість поверхів у будинку, а не конкретний поверх квартири).
     * </p>
     *
     * @param params список текстових параметрів оголошення
     * @return номер поверху (або кількість поверхів для будинку), або {@code -1}, якщо не знайдено
     */
    public static int getFloor(List<String> params) {
        int floor = extractInteger(params, "Поверх:");
        return floor >= 0 ? floor : extractInteger(params, "Поверховість:");
    }

    /**
     * Вилучає загальну площу об'єкта нерухомості зі списку параметрів.
     * <p>
     * Спочатку шукає стандартний параметр "Загальна площа:" (квартири, будинки, комерція — у м²).
     * Якщо його немає (типовий випадок для земельних ділянок, де OLX замість цього вказує
     * "Площа ділянки: X соток") — бере значення звідти й конвертує соток → м² (1 сотка = 100 м²),
     * щоб поле total_area лишалось в ОДНІЙ одиниці виміру для всіх категорій.
     * </p>
     *
     * @param params список текстових параметрів оголошення
     * @return значення загальної площі у кв.м, або {@code -1.0}, якщо жоден з параметрів не знайдено чи пошкоджений
     */
    public static double getTotalArea(List<String> params) {
        String value = extractString(params, "Загальна площа:");
        if (value != null) {
            double m2 = parseDoubleSafe(value);
            if (m2 >= 0) return m2;
        }

        // Земельні ділянки на OLX не мають "Загальна площа:" — там окремий параметр в сотках
        double sotka = getLandAreaSotka(params);
        if (sotka >= 0) return sotka * 100.0; // 1 сотка = 100 м²

        return -1;
    }

    /**
     * Вилучає площу земельної ділянки в сотках "як є" (без конвертації), для випадків,
     * коли потрібне саме звичне для покупців землі значення (наприклад, у форматуванні
     * оголошення "12 соток", а не "1200 м²").
     *
     * @param params список текстових параметрів оголошення
     * @return площа ділянки в сотках, або {@code -1.0}, якщо параметр відсутній чи пошкоджений
     */
    public static double getLandAreaSotka(List<String> params) {
        String value = extractString(params, "Площа ділянки:");
        if (value == null) return -1;
        return parseDoubleSafe(value);
    }

    /**
     * Безпечний парсинг дробового числа з рядка параметра: залишає лише цифри та крапку.
     */
    private static double parseDoubleSafe(String value) {
        try {
            String num = value.replaceAll("[^\\d.]", "");
            return num.isEmpty() ? -1 : Double.parseDouble(num);
        } catch (NumberFormatException e) {
            return -1;
        }
    }

    /**
     * Внутрішній допоміжний метод для вилучення цілого числа з параметра за заданим префіксом.
     * <p>
     * Видаляє з відповідного рядка всі нецифрові символи перед парсингом.
     * </p>
     *
     * @param params список параметрів
     * @param prefix текстовий префікс для пошуку (наприклад, "Поверх:")
     * @return ціле числове значення, або {@code -1} у разі помилки
     */
    private static int extractInteger(List<String> params, String prefix) {
        String value = extractString(params, prefix);
        if (value == null) {
            return -1;
        }
        try {
            // Регулярний вираз [^\d] замінює будь-який символ, що не є цифрою, на порожній рядок
            String num = value.replaceAll("[^\\d]", "");
            return num.isEmpty() ? -1 : Integer.parseInt(num);
        } catch (NumberFormatException e) {
            return -1;
        }
    }

    /**
     * Знаходить у списку параметрів рядок, що починається з вказаного префікса.
     *
     * @param params список текстових параметрів
     * @param prefix шуканий префікс на початку рядка
     * @return повний знайдений рядок параметру (наприклад, "Поверх: 9"), або {@code null}, якщо збігів не знайдено
     */
    private static String extractString(List<String> params, String prefix) {
        if (params == null) {
            return null;
        }
        for (String param : params) {
            if (param.startsWith(prefix)) {
                return param;
            }
        }
        return null;
    }
}
