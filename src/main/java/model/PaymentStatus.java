package model;

/**
 * Перерахування (Enum) статусів оплати за розміщення оголошення, створеного користувачем
 * через майстер {@code CreatePostWizardController}.
 *
 * @author Mykola
 */
public enum PaymentStatus {

    /** Оголошення сформовано, очікується надходження оплати з боку користувача. */
    PENDING("Очікує оплати"),

    /** Оплата підтверджена (вручну адміністратором або автоматично за виписками). */
    PAID("Оплачено"),

    /** Користувач скасував публікацію до моменту оплати. */
    CANCELLED("Скасовано"),

    /** Час на оплату сплив (наприклад, код платежу протух). */
    EXPIRED("Прострочено");

    /** Людська мітка статусу для відображення в інтерфейсі бота чи адмінці. */
    private final String label;

    PaymentStatus(String label) {
        this.label = label;
    }

    /**
     * @return текстова назва статусу оплати українською мовою
     */
    public String getLabel() {
        return label;
    }
}
