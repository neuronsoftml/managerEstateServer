package model;

/**
 * Перерахування (Enum) міст та передмість для пошуку нерухомості на OLX.
 * <p>
 * Кожен елемент містить унікальний ідентифікатор (slug), який використовується
 * для побудови URL-запиту до OLX, та людську назву (label) для відображення в інтерфейсі й логах.
 * </p>
 * * <b>Як знайти новий slug:</b>
 * <ol>
 * <li>Відкрийте пошук за потрібним містом на сайті <a href="https://www.olx.ua">olx.ua</a>.</li>
 * <li>Скопіюйте сегмент URL після назви категорії (наприклад, у посиланні
 * {@code .../prodazha-kvartir/chernovtsy/} сегментом буде {@code chernovtsy}).</li>
 * </ol>
 * * @author Mykola
 */
public enum City {

    // ── ОСНОВНІ МІСТА ────────────────────────────────────────────────────────

    /** Обласний центр Чернівці */
    CHERNIVTSI    ("chernovtsy",              "Чернівці"),

    /** Село Годилів (Чернівецький район) */
    GODILIV       ("godilov",                 "Годилів"),


    // ── ТОПОВІ ПЕРЕДМІСТЯ ЧЕРНІВЦІВ (ДЕ БАГАТО КВАРТИР/НОВОБУДОВ) ────────────

    /** Село Коровія */
    KOROVIA       ("koroviya",                "Коровія"),

    /** Село Чагор */
    CHAGOR        ("chagor",                  "Чагор");

    /** Сегмент шляху в URL OLX для конкретного населеного пункту */
    private final String slug;

    /** Офіційна назва населеного пункту українською мовою */
    private final String label;

    /**
     * Конструктор для ініціалізації елементів перерахування міста.
     *
     * @param slug  частина URL-адреси на OLX
     * @param label відображувана назва міста
     */
    City(String slug, String label) {
        this.slug  = slug;
        this.label = label;
    }

    /**
     * Повертає URL-сегмент (slug) міста для формування запитів до парсера.
     *
     * @return текстовий ідентифікатор міста в системі OLX (наприклад, "chernovtsy")
     */
    public String getSlug()  { return slug; }

    /**
     * Повертає красиву назву міста українською мовою для виведення в інтерфейс бота або консоль.
     *
     * @return назва міста (наприклад, "Чернівці")
     */
    public String getLabel() { return label; }

    /**
     * Статичний метод для пошуку відповідного об'єкта {@link City} у сирому тексті.
     * Використовується для розпізнавання міста з неструктурованих даних (наприклад, з тексту оголошення або картки).
     *
     * @param rawCity сирий рядок, що може містити назву міста (наприклад, "Чернівецька область, Чернівці, Першотравневий")
     * @return знайдений об'єкт {@link City}, або {@code null}, якщо збігів не виявлено
     */
    public static City findByRawString(String rawCity) {
        if (rawCity == null) return null;

        // Перебираємо всі доступні міста та шукаємо входження їхньої мітки (label) у вхідний рядок
        for (City city : City.values()) {
            if (rawCity.contains(city.getLabel())) {
                return city;
            }
        }
        return null; // Повертаємо null, якщо місто не вдалося ідентифікувати
    }
}