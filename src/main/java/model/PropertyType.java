package model;

/**
 * Перерахування (Enum) типів нерухомості.
 * <p>
 * Визначає конкретну категорію об'єкта нерухомості на другому кроці покрокового пошуку в боті.
 * Доступні типи залежать від обраного напрямку угоди {@link DealType}.
 * </p>
 * <p><b>⚠️ ВАЖЛИВА ПРИМІТКА ЩОДО РОЗРОБКИ:</b><br>
 * Наразі модуль збору даних з OLX (клас {@code OlxController}) налаштований на імпорт
 * виключно категорії квартир. Тому в базі даних зберігається інформація <b>тільки</b> для типу {@link #APARTMENT}.
 * Для інших типів ({@link #HOUSE}, {@link #COMMERCIAL}, {@link #LAND}) архітектура навігації
 * та меню в боті вже повністю реалізовані, але виведення результатів тимчасово показує
 * інформаційну заглушку "у розробці", доки парсер не буде розширено під нові категорії OLX.
 * </p>
 * * @author Mykola
 */
public enum PropertyType {

    /** Квартири (кімнати, апартаменти тощо) — повністю робочий розділ */
    APARTMENT ("Квартира"),

    /** Приватні будинки, дачі, таунхауси (тимчасово в розробці) */
    HOUSE     ("Будинок"),

    /** Офіси, склади, магазини, промислові приміщення (тимчасово в розробці) */
    COMMERCIAL("Комерційна нерухомість"),

    /** Ділянки під забудову, ОСГ, садівництво (тимчасово в розробці) */
    LAND      ("Земельна ділянка");

    /** Відображувана назва типу нерухомості українською мовою для кнопок та інтерфейсу */
    private final String label;

    /**
     * Конструктор для ініціалізації елементів перерахування типу нерухомості.
     *
     * @param label текстова назва категорії
     */
    PropertyType(String label) {
        this.label = label;
    }

    /**
     * Повертає назву типу нерухомості українською мовою для побудови меню бота або логування.
     *
     * @return текстова мітка (наприклад, "Квартира", "Будинок")
     */
    public String getLabel() {
        return label;
    }
}
