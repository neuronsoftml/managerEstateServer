package model;

/**
 * Перерахування (Enum) типів угод на ринку нерухомості.
 * <p>
 * Визначає глобальний напрямок фінансової операції для оголошення чи пошукового запиту
 * (наприклад, чи користувач хоче придбати об'єкт у власність, чи взяти його в оренду).
 * </p>
 * * @author Mykola
 */
public enum DealType {

    /** Купівля/продаж об'єкта нерухомості (перехід права власності) */
    BUY("Купівля"),

    /** Оренда об'єкта нерухомості (тимчасове користування) */
    RENT("Оренда");

    /** Людська мітка (назва) типу угоди українською мовою для інтерфейсу */
    private final String label;

    /**
     * Конструктор для ініціалізації елементів перерахування типу угоди.
     *
     * @param label відображувана назва типу угоди
     */
    DealType(String label) {
        this.label = label;
    }

    /**
     * Повертає назву типу угоди українською мовою для відображення в інтерфейсі бота або логах.
     *
     * @return текстова мітка (наприклад, "Купівля" або "Оренда")
     */
    public String getLabel() {
        return label;
    }
}
